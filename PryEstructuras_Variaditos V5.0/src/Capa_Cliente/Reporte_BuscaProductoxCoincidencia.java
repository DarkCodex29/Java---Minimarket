     void renderGrafico(ListaEnlazada lista){
               DefaultPieDataset data = new DefaultPieDataset();
          for (int i = 0; i < lista.tamaño(); i++) {
             Coincidencia objCoincidencia = (Coincidencia) lista.Buscar(i);
             data.setValue(objCoincidencia.getMes(), objCoincidencia.monto);
         }
            JFreeChart chart = ChartFactory.createPieChart(
                    "grafico",data, true, true, false);

            // Mostrar Grafico
            ChartFrame frame = new ChartFrame("GRAFICO", chart);
            frame.pack();
            frame.setVisible(true);
     }

