   private DefaultTableModel renderTable(ListaEnlazada lista){
            DefaultTableModel modelo = new DefaultTableModel();
        ListaEnlazada LP;

        modelo.addColumn("ID Prod.");
        modelo.addColumn("Nombre");
        modelo.addColumn("Estado");
        modelo.addColumn("Categoria");
        modelo.addColumn("T. Unidad");
        modelo.addColumn("Cantidad");
        modelo.addColumn("Precio");

        Object datos[] = new Object[7];

        LP = lista;

        for (int i = 0; i < LP.tamaño(); i++) {
            Producto objCliente = (Producto) LP.Buscar(i);
            datos[0] = objCliente.getCodProducto();
            datos[1] = objCliente.getNombre();
            datos[2] = objCliente.getEstado();
            datos[3] = objCliente.getCategoria();
            datos[4] = objCliente.getUnidad();
            datos[5] = objCliente.getCantidad();
            datos[6] = objCliente.getPrecio();

            modelo.addRow(datos);
        } 
       return modelo;
   }
   
      private DefaultTableModel renderTableCoincidencia(ListaEnlazada lista){
            DefaultTableModel modelo = new DefaultTableModel();
        ListaEnlazada LP;

        modelo.addColumn("fecha");
        modelo.addColumn("mes");
        modelo.addColumn("monto");
 
       Object datos[] = new Object[3];

        LP = lista;

        for (int i = 0; i < LP.tamaño(); i++) {
            Coincidencia objCliente = (Coincidencia) LP.Buscar(i);
            datos[0] = objCliente.fecha;
            datos[1] = objCliente.getMes();         
            datos[2]  = objCliente.monto;
            modelo.addRow(datos);
        } 
       return modelo;
   }
